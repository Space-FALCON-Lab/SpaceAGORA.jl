//! \brief Copy tabular data into height model data fields.
//!
//! Initialization of static data need only be performed once.
void JupiterModel::initializeData() {
  if (initialized) {
    return;
  }
  initialized = true;

  mHeight.resize(heightSize);
  mTemp.resize(heightSize);
  mPres.resize(heightSize);
  mDens.resize(heightSize);
  for (size_t i = 0; i < heightSize; ++i) {
    mHeight[i] = data[i].z;
    mTemp[i] = data[i].temp;
    mPres[i] = data[i].pres * 100.0; // mbar to pascal
    mDens[i] = data[i].dens;
  }
}

