//! \brief Populate the lower atmosphere tables.
//!
//! The lower atmosphere data is stored in static structures (shared by all instances).
//! Initially, all data resides in a vector of VIRALowData structs.  This data is relocated
//! and possibly modified into vectors dimensioned by height and latitude.
//!
//! \b Inputs
//! \arg #data The initial structure of the data.
//! \arg #initialized Flags when data has been initialized.
//!
//! \returns The static data tables are populated.
void VenusIRALow::initializeData()
{
  if (initialized) {
    return;
  }
  initialized = true;

  // Reorganize the data table into indexable containers
  // vectors are resizable, resize the first dimension
  vlat.resize(latSize);
  dlo.resize(latSize);
  plo.resize(latSize);
  tlo.resize(latSize);
  Rlo.resize(latSize);
  zetalo.resize(latSize);

  size_t dataIndex = 0;
  // loop over latitudes
  for (size_t latIndex = 0; latIndex < latSize; ++latIndex) {
    // store the latitudes
    vlat[latIndex] = data[dataIndex].lat;
    // resize the second dimension
    dlo[latIndex].resize(heightSize);
    plo[latIndex].resize(heightSize);
    tlo[latIndex].resize(heightSize);
    Rlo[latIndex].resize(heightSize);
    zetalo[latIndex].resize(heightSize);
    // loop over heights
    for (size_t zIndex = 0; zIndex < heightSize; ++zIndex) {
      if (latIndex == 0) {
        // only store the height data once
        zlo.push_back(data[zIndex].z);
      }
      // store the data
      dlo[latIndex][zIndex] = data[dataIndex].dens;
      tlo[latIndex][zIndex] = data[dataIndex].temp;
      Rlo[latIndex][zIndex] = data[dataIndex].rspec;
      zetalo[latIndex][zIndex] = data[dataIndex].zeta;
      // pressure is recomputed
      plo[latIndex][zIndex] = zetalo[latIndex][zIndex] * dlo[latIndex][zIndex] * Rlo[latIndex][zIndex] * tlo[latIndex][zIndex];
      // go to the next row of data
      ++dataIndex;
    }
  }
}

