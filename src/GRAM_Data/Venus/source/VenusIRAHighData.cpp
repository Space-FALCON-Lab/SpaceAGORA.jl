//! \brief Populate the upper atmosphere tables.
//!
//! The upper atmosphere data is stored in static structures (shared by all instances).
//! Initially, all data resides in a vector of VIRAHighData structs.  This data is relocated
//! and possibly modified into vectors dimensioned by height and solar zenith angle.
//!
//! \b Inputs
//! \arg #data The initial structure of the data.
//! \arg #initialized Flags when data has been initialized.
//!
//! \returns The static data tables are populated.
void VenusIRAHigh::initializeData()
{
  if (initialized) {
    return;
  }
  initialized = true;

  // Reorganize the data table into indexable containers
  // vectors are resizable, resize the first dimension
  vsza.resize(szaSize);
  zhi.resize(heightSize);
  phi.resize(szaSize);
  thi.resize(szaSize);
  dhi.resize(szaSize);
  yco2hi.resize(szaSize);
  yn2hi.resize(szaSize);
  yohi.resize(szaSize);
  ycohi.resize(szaSize);
  yhehi.resize(szaSize);
  ynhi.resize(szaSize);
  yhhi.resize(szaSize);

  size_t dataIndex = 0;
  // loop over sza
  for (size_t szaIndex = 0; szaIndex < szaSize; ++szaIndex) {
    // store the sza values
    vsza[szaIndex] = data[dataIndex].sza;
    // resize the second dimension
    dhi[szaIndex].resize(heightSize);
    phi[szaIndex].resize(heightSize);
    thi[szaIndex].resize(heightSize);
    yco2hi[szaIndex].resize(heightSize);
    yn2hi[szaIndex].resize(heightSize);
    yohi[szaIndex].resize(heightSize);
    ycohi[szaIndex].resize(heightSize);
    yhehi[szaIndex].resize(heightSize);
    ynhi[szaIndex].resize(heightSize);
    yhhi[szaIndex].resize(heightSize);
    // loop over heights
    for (size_t zIndex = 0; zIndex < heightSize; ++zIndex) {
      if (szaIndex == 0) {
        // only store the height data once
        zhi[zIndex] = data[dataIndex].z;
      }
      // store the data
      dhi[szaIndex][zIndex] = data[dataIndex].dens;
      phi[szaIndex][zIndex] = data[dataIndex].pres;
      thi[szaIndex][zIndex] = data[dataIndex].temp;
      yco2hi[szaIndex][zIndex] = data[dataIndex].ndco2;
      yn2hi[szaIndex][zIndex] = data[dataIndex].ndn2;
      yohi[szaIndex][zIndex] = data[dataIndex].ndo;
      ycohi[szaIndex][zIndex] = data[dataIndex].ndco;
      yhehi[szaIndex][zIndex] = data[dataIndex].ndhe;
      ynhi[szaIndex][zIndex] = data[dataIndex].ndn;
      yhhi[szaIndex][zIndex] = data[dataIndex].ndh;
      // go to the next row of data
      ++dataIndex;
    }
  }
}

